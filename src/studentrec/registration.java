    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        String name = txtname.getText();
        String enrollment = txtenrollment.getText();
        String branch = txtbranch.getText();
        String semester = txtsemester.getText();
        String mobile = txtmobile.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mystudents", "root", "");
            
            insert =  con.prepareStatement("insert into studentrecord(name,enrollment,branch,semester,mobile) values (?,?,?,?,?)");
            
            insert.setString(1, name);
             insert.setString(2, enrollment);
              insert.setString(3, branch);
               insert.setString(4, semester);
                insert.setString(5, mobile);
              
                   insert.executeUpdate();
                   
                   JOptionPane.showMessageDialog(this,"Record Inserted Successfully");
                   //vw.table_update():
                   txtname.setText("");
                   txtenrollment.setText("");
                   txtbranch.setText("");
                   txtsemester.setText("");
                   txtmobile.setText("");
                   
                   txtname.requestFocus();
            
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(registration.class.getName()).log(Level.SEVERE, null, ex);  
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

